package ex_04_Operators;

public class Lab036_Operators_Arithmetic {


    // + Addition
    // - Subtraction
    // * Multiplication
    // / Division
    // % Modulus

    public static void main(String[] args) {

        int a = 20;
        int b = 2;
        float c = 3.0f;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);


        System.out.println(a/c);
        System.out.println("a+b");
        System.out.println(a+b);
    }
}
